    private void loadDataToTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Hapus data lama

    try {
        String sql = "SELECT * FROM db_penjualan";
        Statement stmt = Con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"), // ID sebagai kolom pertama
                rs.getString("Nama"),
                rs.getString("Paket_Makanan"),
                rs.getString("Menu_Makanan"),
                rs.getInt("Harga"),
                rs.getInt("Jumlah"),
                rs.getInt("Total_Belanja"),
                rs.getDouble("Diskon"),
                rs.getDouble("Harus_Dibayar"),
                rs.getInt("Jumlah_Bayar"),
                rs.getDouble("Kembalian")
            });
        }

        // Sembunyikan kolom ID (kolom 0)
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setWidth(0);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
    }
}


