    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    try {
        Statement st = Con.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT * FROM db_penjualan WHERE nama LIKE '%" + txtsearch.getText() + "%'"
        );

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"), // WAJIB di index pertama
                rs.getString("nama"),
                rs.getString("paket_makanan"),
                rs.getString("menu_makanan"),
                rs.getInt("harga"),
                rs.getInt("jumlah"),
                rs.getInt("total_belanja"),
                rs.getDouble("diskon"),
                rs.getDouble("harus_dibayar"),
                rs.getDouble("jumlah_bayar"),
                rs.getDouble("kembalian")
            });
        }

    } catch (Exception e) {
        System.out.println(e);
    }
    }//GEN-LAST:event_btnsearchActionPerformed

