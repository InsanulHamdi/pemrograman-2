    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int jumlah;
        int harga;
        int totalbelanja;
        double diskon;
        
        jumlah=Integer.parseInt(txtJumlah.getText());
        harga=Integer.parseInt(txtHarga.getText());
        totalbelanja=jumlah*harga;
        txtTotalBelanja.setText(Integer.toString(totalbelanja));
        
        if (jumlah>=2){
            diskon=0.1*totalbelanja;
            txtDiskon.setText(Double.toString(diskon));
            harusdibayar=totalbelanja-diskon;
            txtHarusdiBayar.setText(Double.toString(harusdibayar));
            txtJumlahBayar.requestFocus();
        }
        else{
            diskon=0*totalbelanja;
            txtDiskon.setText(Double.toString(diskon));
            harusdibayar=totalbelanja-diskon;
            txtHarusdiBayar.setText(Double.toString(harusdibayar));
            txtJumlahBayar.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

